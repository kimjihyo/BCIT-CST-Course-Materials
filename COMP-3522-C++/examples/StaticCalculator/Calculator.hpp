#ifndef CALCULATOR_CALCULATOR_HPP
#define CALCULATOR_CALCULATOR_HPP
struct Calculator {
    static int add(int num1, int num2)
    {
        return num1 + num2;
    }

    static int subtract(int num1, int num2)
    {
        return num1 - num2;
    }
};
#endif //CALCULATOR_CALCULATOR_HPP
